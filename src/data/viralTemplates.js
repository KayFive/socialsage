export const getTemplateById = (id) => {
  return VIRAL_TEMPLATES[id] || null;
};

export const getTemplatesByCategory = (category) => {
  return Object.values(VIRAL_TEMPLATES).filter(template => 
    template.category === category
  );
};

export const getTemplatesForNiche = (niche) => {
  return Object.values(VIRAL_TEMPLATES).filter(template =>
    template.best_for.some(n => n.toLowerCase().includes(niche.toLowerCase()))
  );
};